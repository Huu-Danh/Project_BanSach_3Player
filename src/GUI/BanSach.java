    boolean checkMaSach(int maSach) {
        for (Chi_tiet_hd cthd : cthds) {
            if (maSach == cthd.getSach().getMaSach()) {
                return false;
            }
        }
        return true;
    }

    void addCTHDToTable(ArrayList<Chi_tiet_hd> cthds) {
        modelTable.setRowCount(0);
        double TongThanhTien = 0;
        DecimalFormat decimalFormat = new DecimalFormat("#,### VND");
        for (Chi_tiet_hd cthd : cthds) {
            String StrGia = decimalFormat.format(cthd.getSach().getGia());
            String StrTongTien = decimalFormat.format(cthd.getTongTien());
            modelTable.addRow(new Object[]{cthd.getSach().getTenSach(), StrGia, cthd.getSoLuong(), StrTongTien});
            TongThanhTien += cthd.getTongTien();
            String StrTongThanhTien = decimalFormat.format(TongThanhTien);
            this.txtTongThanhTien.setText("Tổng thành tiền: " + StrTongThanhTien);
            tbHoaDon.updateUI();
        }
        GanTongThanhTien(TongThanhTien);
    }

    Chi_tiet_hd FindCTHD(Sach s) {
        for (Chi_tiet_hd cthd : cthds) {
            if (cthd.getSach().equals(s)) {
                return cthd;
            }
        }
        return null;
    }

    void GanTongThanhTien(double TongTT) {
        for (Chi_tiet_hd cthd : cthds) {
            cthd.getHoaDon().setTongThanhTien(TongTT);
        }
    }

    void RemoveSachToTable(Sach s) {
        modelTable.removeRow(selectedRow);
        cthds.remove(FindCTHD(s));
        DecimalFormat decimalFormat = new DecimalFormat("#,### VND");
        double TongThanhTien = 0;
        for (Chi_tiet_hd cthd : cthds) {
            TongThanhTien += cthd.getTongTien();
        }
        tbHoaDon.updateUI();
        String StrTongThanhTien = decimalFormat.format(TongThanhTien);
        this.txtTongThanhTien.setText("Tổng thành tiền: " + StrTongThanhTien);
        GanTongThanhTien(TongThanhTien);
    }

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int soluong = 0;
        double tongtien = 0;
        if (checkMaSach(sach.getMaSach())) {
            if (!this.txtSoLuong.getText().trim().isEmpty()) {
                soluong = Integer.parseInt(this.txtSoLuong.getText());
                tongtien = soluong * sach.getGia();
                Chi_tiet_hd cthd = new Chi_tiet_hd(soluong, sach, hoaDon, tongtien);
                cthds.add(cthd);
                addCTHDToTable(cthds);
                this.txtMaSach.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Số lượng rỗng", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Mã sách đã tồn tại", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnThemActionPerformed

