    Sach findSach(String tenSach) {
        for (Chi_tiet_hd cthd : cthds) {
            if (cthd.getSach().getTenSach().toLowerCase().equals(tenSach.toLowerCase())) {
                return cthd.getSach();
            }
        }
        return null;
    }
